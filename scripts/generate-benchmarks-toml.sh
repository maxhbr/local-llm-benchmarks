#!/usr/bin/env bash
# Generate per-(producer, backend) benchmark TOML files from an OpenAI-compatible
# endpoint.
#
# Two source shapes are supported, selected by ENDPOINT_BACKEND:
#
#   litellm (default): queries /model/info; each model carries
#     .litellm_params.tags including a producer ("rtx5090"/"gfx1151") and a
#     backend ("cuda"/"vulkan"/"rocm"). One output file is produced per
#     (producer, backend) pair discovered among base-tagged models.
#
#   direct: queries /models (a plain llama-swap / llama-server host). The
#     producer and backend are not always present in the tags, so they must be
#     supplied via ENDPOINT_PRODUCER and ENDPOINT_BACKEND_LABEL. Models tagged
#     "base" are selected; if no model carries any tags at all, every model is
#     included.
#
# Output filename:
#     benchmarks.<ENDPOINT_BACKEND>.<producer>.<backend>.toml

set -euo pipefail

ENDPOINT_URL="${ENDPOINT_URL:-http://litellm.thing.vserver.wg0.maxhbr.local/v1}"
ENDPOINT_NAME="${ENDPOINT_NAME:-litellm.thing.vserver.wg0}"
ENDPOINT_BACKEND="${ENDPOINT_BACKEND:-litellm}"
ENABLE_VARIANTS="${ENABLE_VARIANTS:-false}"
ENDPOINT_PRODUCER="${ENDPOINT_PRODUCER:-}"
ENDPOINT_BACKEND_LABEL="${ENDPOINT_BACKEND_LABEL:-}"
API_KEY="${API_KEY:-EMPTY}"
OUTPUT_DIR_VALUE="${OUTPUT_DIR_VALUE:-./benchmarks}"
BENCHMARKS="${BENCHMARKS:-smoke, llama-benchy}"
VARIANTS_BENCHMARKS="smoke, llama-benchy, llama-benchmark, llama-benchmark-v1, llama-benchmark-v2"

SCRIPT_DIR="$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")" &>/dev/null && pwd)"
REPO_ROOT="$(cd -- "${SCRIPT_DIR}/.." &>/dev/null && pwd)"
OUT_DIR="${OUT_DIR:-${REPO_ROOT}}"

case "$ENDPOINT_BACKEND" in
    litellm) INFO_URL="${ENDPOINT_URL%/}/model/info" ;;
    direct)  INFO_URL="${ENDPOINT_URL%/}/models" ;;
    *)
        echo "Unknown ENDPOINT_BACKEND='${ENDPOINT_BACKEND}' (expected: litellm, direct)" >&2
        exit 2
        ;;
esac

tmpjson="$(mktemp)"
trap 'rm -f "$tmpjson"' EXIT

echo "Fetching model info from ${INFO_URL}..." >&2
curl -fsS "$INFO_URL" -o "$tmpjson"

# Convert "a, b, c" -> TOML array literal: ["a", "b", "c"]
benchmarks_toml_array() {
    local raw="$1"
    local IFS=','
    local -a parts
    read -r -a parts <<<"$raw"
    local out="["
    local first=1
    for p in "${parts[@]}"; do
        p="${p#"${p%%[![:space:]]*}"}"
        p="${p%"${p##*[![:space:]]}"}"
        [[ -z "$p" ]] && continue
        if (( first )); then
            out+="\"$p\""
            first=0
        else
            out+=", \"$p\""
        fi
    done
    out+="]"
    printf '%s' "$out"
}

BENCHMARKS_ARRAY="$(benchmarks_toml_array "$BENCHMARKS")"

write_toml() {
    local producer="$1" backend="$2" filter_desc="$3"
    shift 3
    local -a models=("$@")
    local variants_suffix=""
    if $ENABLE_VARIANTS; then
        variants_suffix=".variants"
    fi
    local out_file="${OUT_DIR}/benchmarks.${ENDPOINT_BACKEND}.${producer}.${backend}${variants_suffix}.toml"

    echo "Writing ${out_file} (${#models[@]} models)" >&2
    {
        printf '# Auto-generated by scripts/generate-benchmarks-toml.sh\n'
        printf '# Endpoint backend: %s\n' "$ENDPOINT_BACKEND"
        printf '# Producer: %s   Backend: %s\n' "$producer" "$backend"
        printf '# Source: %s\n' "$INFO_URL"
        printf '# Filter: %s\n\n' "$filter_desc"
        printf 'output_dir = "%s"\n\n' "$OUTPUT_DIR_VALUE"
        printf '[[endpoints]]\n'
        printf 'name       = "%s"\n' "$ENDPOINT_NAME"
        printf 'url        = "%s"\n' "$ENDPOINT_URL"
        printf 'api_key    = "%s"\n' "$API_KEY"
        printf 'benchmarks = %s\n\n' "$BENCHMARKS_ARRAY"
        for m in "${models[@]}"; do
            printf '  [[endpoints.models]]\n'
            printf '  name = "%s"\n' "$m"
        done
    } >"$out_file"
}

if [[ "$ENDPOINT_BACKEND" == "litellm" ]]; then
    if $ENABLE_VARIANTS; then
        # Use variant benchmarks when flag is enabled
        BENCHMARKS="${VARIANTS_BENCHMARKS}"
    fi
    BENCHMARKS_ARRAY="$(benchmarks_toml_array "${BENCHMARKS}")"

    # Discover (producer, backend) pairs present among base-tagged models.
    mapfile -t pairs < <(
        jq -r '
            .data[]
            | select(.litellm_params.tags | index("base"))
            | .litellm_params.tags as $t
            | ( ["rtx5090","gfx1151"] | map(select(. as $p | $t | index($p))) | .[0] ) as $producer
            | ( ["cuda","vulkan","rocm"] | map(select(. as $b | $t | index($b))) | .[0] ) as $backend
            | select($producer != null and $backend != null)
            | "\($producer)\t\($backend)"
        ' "$tmpjson" | sort -u
    )

    if [[ ${#pairs[@]} -eq 0 ]]; then
        echo "No base-tagged models found." >&2
        exit 1
    fi

    for pair in "${pairs[@]}"; do
        producer="${pair%%	*}"
        backend="${pair##*	}"

        mapfile -t models < <(
            jq -r --arg producer "$producer" --arg backend "$backend" '
                .data[]
                | select(.litellm_params.tags | index("base"))
                | select(.litellm_params.tags | index($producer))
                | select(.litellm_params.tags | index($backend))
                | .model_name
            ' "$tmpjson" | sort -u
        )
        [[ ${#models[@]} -eq 0 ]] && continue

        write_toml "$producer" "$backend" \
            "litellm_params.tags contains \"base\" + \"${producer}\" + \"${backend}\"" \
            "${models[@]}"
    done

else
    # direct: producer/backend can't be discovered reliably, so require them.
    if [[ -z "$ENDPOINT_PRODUCER" || -z "$ENDPOINT_BACKEND_LABEL" ]]; then
        echo "ENDPOINT_BACKEND=direct requires ENDPOINT_PRODUCER and ENDPOINT_BACKEND_LABEL" >&2
        exit 2
    fi

    has_tags="$(jq '[.data[] | select(has("tags")) | .tags // [] | length] | add // 0' "$tmpjson")"

    if [[ "$has_tags" -gt 0 ]]; then
        mapfile -t models < <(
            jq -r '.data[] | select((.tags // []) | index("base")) | .id' "$tmpjson" | sort -u
        )
        filter_desc="tags contains \"base\""
    else
        mapfile -t models < <(
            jq -r '.data[].id' "$tmpjson" | sort -u
        )
        filter_desc="all models (no tags exposed by endpoint)"
    fi

    if [[ ${#models[@]} -eq 0 ]]; then
        echo "No models found." >&2
        exit 1
    fi

    write_toml "$ENDPOINT_PRODUCER" "$ENDPOINT_BACKEND_LABEL" "$filter_desc" "${models[@]}"
fi

echo "Done." >&2
