#!/usr/bin/env python3
"""Extract and rank models by speed from llama-benchy.md benchmark files.

Writes a Markdown report (default: ``benchmarks/find-fastest.md``) with:

* fastest models by single-request generation speed (tg128 c1)
* fastest models by prefill speed (pp2048 c1)
* top models per GPU (tg128 c1)
* top models per size class (tg128 c1)

Usage:
    python3 scripts/find-fastest.py
    python3 scripts/find-fastest.py --benchmarks-dir benchmarks/
    python3 scripts/find-fastest.py -o benchmarks/find-fastest.md
    python3 scripts/find-fastest.py --stdout
"""

from __future__ import annotations

import argparse
import glob
import io
import os
import re
import sys
from dataclasses import dataclass
from typing import Optional, TextIO


@dataclass
class BenchmarkResult:
    model_name: str
    gpu: str
    test: str
    tps_total: Optional[float]
    tps_req: Optional[float]
    peak_tps: Optional[float]
    ttfr: Optional[float]
    est_ppt: Optional[float]


def parse_markdown_table(filepath: str) -> list[BenchmarkResult]:
    """Parse a llama-benchy.md markdown table into structured results."""
    results: list[BenchmarkResult] = []
    with open(filepath, "r") as f:
        lines = f.readlines()

    in_table = False
    for line in lines:
        line = line.strip()
        if not line.startswith("|"):
            in_table = False
            continue

        parts = [p.strip() for p in line.split("|")]
        # parts[1]=model, parts[2]=test, parts[3]=tps_total, parts[4]=tps_req,
        # parts[5]=peak_tps, parts[6]=peak_tps_req, parts[7]=ttfr,
        # parts[8]=est_ppt, parts[9]=e2e
        if len(parts) > 1 and (parts[1] == "model" or parts[1] == "test"):
            in_table = True
            continue
        if any(":-" in p for p in parts):
            continue

        if in_table and len(parts) >= 10:
            model_full = parts[1]
            test = parts[2]

            def parse_metric(s: str) -> Optional[float]:
                s = s.strip()
                if not s or s == "-":
                    return None
                # "3211.28 ± 186.22" -> 3211.28
                match = re.search(r"([\d.]+)", s)
                return float(match.group(1)) if match else None

            tps_total = parse_metric(parts[3])
            tps_req = parse_metric(parts[4])
            peak_tps = parse_metric(parts[5])
            ttfr = parse_metric(parts[7])
            est_ppt = parse_metric(parts[8])

            if ":" in model_full:
                gpu, model_name = model_full.split(":", 1)
            else:
                gpu = "unknown"
                model_name = model_full

            results.append(BenchmarkResult(
                model_name=model_name,
                gpu=gpu,
                test=test,
                tps_total=tps_total,
                tps_req=tps_req,
                peak_tps=peak_tps,
                ttfr=ttfr,
                est_ppt=est_ppt,
            ))

    return results


def extract_model_size(model_name: str) -> Optional[float]:
    """Try to extract model size in billions (e.g., '9B' -> 9)."""
    match = re.search(r"(\d+(?:\.\d+)?)B", model_name)
    return float(match.group(1)) if match else None


def _fmt(v: Optional[float], spec: str = ".2f") -> str:
    if v is None:
        return "-"
    return format(v, spec)


def _write_table(out: TextIO, headers: list[str], rows: list[list[str]]) -> None:
    out.write("| " + " | ".join(headers) + " |\n")
    out.write("|" + "|".join("---" for _ in headers) + "|\n")
    for r in rows:
        out.write("| " + " | ".join(r) + " |\n")
    out.write("\n")


def render_report(all_results: list[BenchmarkResult], out: TextIO, source_count: int) -> None:
    out.write("# Fastest models\n\n")
    out.write(
        f"Auto-generated by `scripts/find-fastest.py` from {source_count} "
        "`llama-benchy.md` file(s).\n\n"
    )

    # --- tg128 (c1) at base context -------------------------------------------------
    tg128_c1 = [
        r for r in all_results
        if "tg128" in r.test
        and "(c1)" in r.test
        and "@ d" not in r.test
        and r.tps_total
    ]
    tg128_c1.sort(key=lambda r: r.tps_total or 0, reverse=True)

    out.write("## Fastest by single-request generation speed (tg128 c1)\n\n")
    rows: list[list[str]] = []
    for i, r in enumerate(tg128_c1, 1):
        size = extract_model_size(r.model_name)
        rows.append([
            str(i),
            r.model_name,
            r.gpu,
            _fmt(r.tps_total),
            _fmt(r.tps_req),
            _fmt(r.peak_tps),
            f"{size}B" if size else "N/A",
        ])
    _write_table(
        out,
        ["Rank", "Model", "GPU", "t/s (total)", "t/s (req)", "Peak t/s", "Size"],
        rows,
    )

    # --- pp2048 (c1) at base context ------------------------------------------------
    pp2048_c1 = [
        r for r in all_results
        if "pp2048" in r.test
        and "(c1)" in r.test
        and "@ d" not in r.test
        and r.tps_total
    ]
    pp2048_c1.sort(key=lambda r: r.tps_total or 0, reverse=True)

    out.write("## Fastest by prefill speed (pp2048 c1)\n\n")
    rows = []
    for i, r in enumerate(pp2048_c1, 1):
        size = extract_model_size(r.model_name)
        rows.append([
            str(i),
            r.model_name,
            r.gpu,
            _fmt(r.tps_total),
            _fmt(r.ttfr),
            _fmt(r.est_ppt),
            f"{size}B" if size else "N/A",
        ])
    _write_table(
        out,
        ["Rank", "Model", "GPU", "t/s (total)", "TTFR (ms)", "est_ppt (ms)", "Size"],
        rows,
    )

    # --- Top per GPU ---------------------------------------------------------------
    out.write("## Top models per GPU (tg128 c1)\n\n")
    gpus = sorted({r.gpu for r in tg128_c1})
    for gpu in gpus:
        gpu_results = sorted(
            (r for r in tg128_c1 if r.gpu == gpu),
            key=lambda r: r.tps_total or 0,
            reverse=True,
        )
        out.write(f"### {gpu} ({len(gpu_results)} models benchmarked)\n\n")
        rows = []
        for i, r in enumerate(gpu_results[:10], 1):
            size = extract_model_size(r.model_name)
            rows.append([
                str(i),
                r.model_name,
                _fmt(r.tps_total),
                f"{size}B" if size else "N/A",
            ])
        _write_table(out, ["Rank", "Model", "t/s (total)", "Size"], rows)

    # --- Top per size class --------------------------------------------------------
    out.write("## Top models per size class (tg128 c1)\n\n")
    sized = [
        (r, extract_model_size(r.model_name))
        for r in tg128_c1
        if extract_model_size(r.model_name)
    ]

    def bucket_for(size: float) -> str:
        if size < 3:
            return "<3B"
        if size < 7:
            return "3-7B"
        if size < 15:
            return "7-15B"
        if size < 35:
            return "15-35B"
        if size < 70:
            return "35-70B"
        if size < 130:
            return "70-130B"
        return ">130B"

    size_classes: dict[str, list[tuple[BenchmarkResult, float]]] = {}
    for r, size in sized:
        size_classes.setdefault(bucket_for(size), []).append((r, size))

    for bucket in ["<3B", "3-7B", "7-15B", "15-35B", "35-70B", "70-130B", ">130B"]:
        entries = size_classes.get(bucket, [])
        if not entries:
            continue
        entries.sort(key=lambda x: x[0].tps_total or 0, reverse=True)
        out.write(f"### {bucket}\n\n")
        rows = []
        for r, size in entries[:10]:
            rows.append([
                r.model_name,
                _fmt(r.tps_total),
                f"{size}B",
                r.gpu,
            ])
        _write_table(out, ["Model", "t/s (total)", "Size", "GPU"], rows)


def main(argv: list[str] | None = None) -> int:
    p = argparse.ArgumentParser(description=__doc__)
    repo_root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    default_bench = os.path.join(repo_root, "benchmarks")
    default_out = os.path.join(default_bench, "find-fastest.md")

    p.add_argument(
        "--benchmarks-dir",
        default=default_bench,
        help=f"Directory containing benchmark subdirs (default: {default_bench})",
    )
    p.add_argument(
        "-o",
        "--output",
        default=default_out,
        help=f"Output Markdown file (default: {default_out})",
    )
    p.add_argument(
        "--stdout",
        action="store_true",
        help="Write report to stdout instead of --output.",
    )
    args = p.parse_args(argv)

    pattern = os.path.join(args.benchmarks_dir, "**", "llama-benchy.md")
    files = glob.glob(pattern, recursive=True)

    if not files:
        print(f"No llama-benchy.md files found under {args.benchmarks_dir}", file=sys.stderr)
        return 1

    print(f"Found {len(files)} benchmark files", file=sys.stderr)

    all_results: list[BenchmarkResult] = []
    for filepath in sorted(files):
        all_results.extend(parse_markdown_table(filepath))

    # Deduplicate by (model_name, gpu, test) keeping the row with non-null tps_total.
    seen: dict[tuple[str, str, str], BenchmarkResult] = {}
    for r in all_results:
        key = (r.model_name, r.gpu, r.test)
        if key not in seen or (r.tps_total and not seen[key].tps_total):
            seen[key] = r
    all_results = list(seen.values())

    if args.stdout:
        render_report(all_results, sys.stdout, source_count=len(files))
    else:
        # Render into a buffer first so we never leave a half-written file on error.
        buf = io.StringIO()
        render_report(all_results, buf, source_count=len(files))
        os.makedirs(os.path.dirname(os.path.abspath(args.output)) or ".", exist_ok=True)
        with open(args.output, "w", encoding="utf-8") as f:
            f.write(buf.getvalue())
        print(f"Wrote {args.output}", file=sys.stderr)

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
