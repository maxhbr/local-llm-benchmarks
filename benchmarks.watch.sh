#!/usr/bin/env bash
# Watch the benchmarks/ folder and regenerate derived files whenever a file
# changes.  A simple HTTP server runs in parallel (background) so the
# generated dashboard stays live while you work.
#
# What it does:
#   1. runs ./benchmarks.update.sh --no-serve once  (so outputs are fresh)
#   2. starts `python3 -m http.server` in the background (port $HTTP_PORT)
#   3. watches benchmarks/ with `entr`, re-running the update on every change
#
# The generated output files (datasets.json, llama-benchy.csv, find-fastest.md,
# *.computed.yaml) are excluded from the watch list so the regeneration does
# not re-trigger itself in a loop.
#
# `entr -d` makes entr exit when files are added/removed; the outer `while`
# loop then rebuilds the watch list (picking up new files) and re-runs the
# update once before resuming the watch.  `-p` (postpone) suppresses entr's
# one-shot startup run so we don't get a redundant regeneration right after
# the initial update.
#
# Usage:
#   ./benchmarks.watch.sh                  # default benchmarks/ dir, port 8000
#   ./benchmarks.watch.sh path/to/bench    # custom benchmarks dir
#   HTTP_PORT=8080 ./benchmarks.watch.sh   # custom port
#
set -euo pipefail

SCRIPT_DIR="$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")" &> /dev/null && pwd)"
cd "$SCRIPT_DIR"

BENCH_DIR="${1:-benchmarks}"
HTTP_PORT="${HTTP_PORT:-8000}"
PY="${PYTHON:-python3}"

if [[ ! -d "$BENCH_DIR" ]]; then
    echo "error: benchmarks directory not found: $BENCH_DIR" >&2
    exit 1
fi

# The update command (regenerate only; the http server is handled below).
UPDATE=("$SCRIPT_DIR/benchmarks.update.sh" --no-serve "$BENCH_DIR")

# --- resolve entr -----------------------------------------------------------
# Prefer a system entr (e.g. from `nix develop`); otherwise transparently pull
# it via nix so the script works without a prepared shell.
if command -v entr >/dev/null 2>&1; then
    ENTR=(entr)
elif command -v nix >/dev/null 2>&1; then
    echo "entr not found on PATH; using 'nix shell nixpkgs#entr' ..." >&2
    ENTR=(nix shell nixpkgs#entr -c entr)
else
    echo "error: 'entr' is required but was not found (install it or use nix)" >&2
    exit 1
fi

# --- helpers ----------------------------------------------------------------
step() { printf '\n\033[1;36m==> %s\033[0m\n' "$*"; }

# Files generated by the update step.  Excluding these from the watch list
# prevents the regeneration from re-triggering itself.  The computed YAML
# files are named <bench_type>.computed.yaml (aider.computed.yaml,
# aider-diff.computed.yaml, ...) so we match the *.computed.yaml suffix.
watch_list() {
    find "$BENCH_DIR" -type f \
        ! -name datasets.json \
        ! -name llama-benchy.csv \
        ! -name find-fastest.md \
        ! -name '*.computed.yaml'
}

# --- 2. http server in the background ---------------------------------------
HTTP_LOG="$(mktemp -t bench-http.XXXXXX.log)"
HTTP_PID=""
cleanup() {
    if [[ -n "$HTTP_PID" ]]; then
        kill "$HTTP_PID" 2>/dev/null || true
        wait "$HTTP_PID" 2>/dev/null || true
    fi
    rm -f "$HTTP_LOG" 2>/dev/null || true
}
trap cleanup EXIT
# On Ctrl-C / kill: tear down the server and stop the watch loop.
trap 'echo; echo "Stopping watch..."; exit 130' INT
trap 'exit 143' TERM

step "starting HTTP server on http://localhost:${HTTP_PORT}/ (background)"
echo "    access log: $HTTP_LOG  (tail -f in another terminal)"
"$PY" -m http.server --directory "$BENCH_DIR" "$HTTP_PORT" >"$HTTP_LOG" 2>&1 &
HTTP_PID=$!
sleep 0.3
if ! kill -0 "$HTTP_PID" 2>/dev/null; then
    echo "warning: HTTP server exited immediately (port ${HTTP_PORT} in use?)." >&2
    echo "         The watch will continue without it. Log:" >&2
    cat "$HTTP_LOG" >&2 || true
    HTTP_PID=""
fi

# --- 3. watch loop ----------------------------------------------------------
step "watching $BENCH_DIR for changes (Ctrl-C to stop)"
# Each iteration: run the update once (the initial regeneration, and also
# covers newly added/removed files when entr restarts after a `-d` exit),
# then hand control to entr until the next change.
while :; do
    "${UPDATE[@]}" || true
    watch_list | "${ENTR[@]}" -d -n -p "${UPDATE[@]}" || true
    # entr exited (directory altered / interrupted); pause before restarting.
    sleep 0.2
done
